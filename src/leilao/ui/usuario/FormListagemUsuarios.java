    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here: git copy
        if (tblUsuarios.getSelectedRowCount() > 0) {
            int opcao = JOptionPane
                    .showConfirmDialog(this, "Confirma a exclusão deste usuario?");

            if (opcao == JOptionPane.YES_OPTION) {
                int[] rows = tblUsuarios.getSelectedRows();

                entity.getTransaction().begin();
                for (int row : rows) {
                    UsuarioModel model = (UsuarioModel) tblUsuarios.getModel();
                    Usuario u = model.getUsuario(row);

                    entity.remove(entity.find(Usuario.class, u.getId()));
                }
                
                try {
                    entity.getTransaction().commit();
                    JOptionPane.showMessageDialog(this, 
                            "Usuario removido com sucesso.");
                    btnPesquisarActionPerformed(evt);
                }catch(RollbackException e) {
                    JOptionPane.showMessageDialog(this, 
                            "Erro ao remover usuario.");
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        // TODO add your handling code here: git copy
        if(evt.getClickCount() == 2 && 
                tblUsuarios.getSelectedRowCount() == 1) {
            UsuarioModel model = (UsuarioModel) tblUsuarios.getModel();
            int row = tblUsuarios.getSelectedRow();
            Usuario u = model.getUsuario(row);
          
            FormCadastroUsuario form = new FormCadastroUsuario((Frame) getParent(), true, entity, u);
            form.setVisible(true);
            
            btnPesquisarActionPerformed(null);
        }
    }//GEN-LAST:event_tblUsuariosMouseClicked

