    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        String filtro = txtEmail.getText();

        Query q = entity.createNamedQuery("Usuario.findByName");
        q.setParameter("nome", "%" + filtro + "%");

        List<Usuario> lista = q.getResultList();

        tblUsuarios.setModel(new UsuarioModel(lista));

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhuma usuario encontrado.");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

